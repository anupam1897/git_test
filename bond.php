      <h3 style="text-align: center;">Equity Investment Agreement</h3>

      <p>&nbsp; &nbsp;<br> <br> <br> </p>

<p></p> THIS EQUITY INVESTMENT AGREEMENT (the "Agreement") is dated as of <strong>31 August 2013</strong>	 between <strong>T-box Pvt. Ltd.</strong>   a India business corporation, having an address at <strong>R -6 / 82 Rajnagar, Ghaziabad </strong> and <strong> Udita, an individual Investor, having an address at M16, Varanasi Uttar Pradesh, India, 233230.</strong> 

WHEREAS, Company is developing technologies that it represents are consistent with the educational, research and economic development objectives of Cornell; and

WHEREAS, Company would benefit from a relationship with Cornell and its Kevin M. McGovern Family Center for Venture Development in the Life Sciences, (the "McGovern Center"), whereby the McGovern Center would provide Company with assistance in accessing elements of the McGovern Center's network of public and private commercialization resources (the "McGovern Center Network"); and

WHEREAS, the McGovern Center is willing to provide such assistance, subject to the terms and conditions of this Agreement;

NOW, THEREFORE, in consideration of the mutual covenants and agreements set forth below, the parties covenant and agree as follows:

    <p><br> </p>

<strong>Article 1. Equity Partner Program</strong>
<br>

1.1 As a part of its equity partnership  with  Company,  the  McGovern  Center agrees, from time to time upon the request of Company, to: <br>

(i)	Provide Company with timely information that the McGovern Center reasonably believes to be of benefit to Company including information related to capital formation, license of office and/or laboratory space on or near the main campus of Cornell, and various forms of public and private commercial and financial assistance that may be available to Company;
<br>
(ii)	Provide support to Company in connection with its efforts to raise capital and form strategic alliances consistent with Company's stated mission;
<br>
(iii)	Serve as a liaison between Company and researchers and clinicians at Cornell; and
<br>


(iv)	Feature Company as one of Cornell's "Equity Partners" in the McGovern Center-related publicity, and permit Company to identify Cornell as an "Equity Partner" in Company promotional or company background material.
<br>
</p>
<strong>Article 2. Consideration</strong> 

<strong> 2.1	In consideration for Cornell's willingness to enter into this Agreement and the services to be rendered to Company hereunder:
</strong>
<p>
    <br>
(i)	Within forty-five (45) days of the execution and delivery of this Agreement Company agrees to issue to Cornell shares of Company voting common stock ("Company Shares''), such number of Company Shares being equivalent to
__ % of Company's currently issued and outstanding voting common stock on a  fully converted, fully diluted basis.
<br>
(ii)	Through arbitration Company shall, concurrently with any
issuance of new Company Shares for the purpose of raising working capital for Company operations, issue to Cornell such number of additional Company Shares as would be necessary to maintain Cornell's percentage share of Company's currently issued and outstanding equity on a fully converted, fully diluted basis. The obligation of Company to issue additional Company Shares to Cornell pursuant to this Section shall: (a) not be required as a result of the issuance of new Company Shares for the purposes of Company acquisitions of assets or businesses or other similar purposes, and (b) terminate upon either (i) a merger between Company and a third party in which Company is not the surviving entity; or (ii) any acquisition of all of Company's assets and liabilities.
<br>
(iii)	In the event that Company's aggregate expenditures on research and development dedicated to enhancing or developing Cornell technologies exceeds 50% of Company's total research and development expenditures during the period
20      through   20   ,  then   Company   shall, by  IPO, issue to Cornell
additional Company Shares in the same quantity as the original Company Shares issued to Cornell and not increased to reflect additional Company Shares issued for the purposes of raising capital, acquisitions or other similar purposes, except that additional Company Shares shall be issued in an amount equal to the Company Shares issued in accordance with Section 2.1 (ii).
<br>
2.2	Within the time periods provided, Company shall deliver, or cause to be delivered, to Cornell a certificate or certificates, registered in Cornell's name, representing all of the Company Shares then required to be issued to Cornell.
 <br>
</p>

