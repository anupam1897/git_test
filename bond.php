Article 3. Confidentiality <br>
</strong>
3.1	Each party acknowledges that the other party, its subsidiaries and affiliated companies are the owners of valuable trade secrets and other confidential information. Each party receiving (a "Party Recipient') information hereunder concerning the business, products, equipment, systems, techniques, and practices that is identified in writing as confidential ("Confidential Information") of the disclosing party (the ''Disclosing Party"), for a period of five years commencing upon first receipt of the particular Confidential Information (the "Confidentiality Period") shall retain the same in confidence and shall only use such Confidential Information for purposes of this Agreement, or as otherwise agreed to in writing by both parties, and shall not disclose any such Confidential Information to any entity; provided, that "Confidential Information" shall not include any information that: (a) was already known to the Party Recipient prior to the date of this Agreement as documented in Party Recipient's records made prior to such dates; (b) was publicly available in its entirety as of the date of the disclosure or thereafter becomes publicly available other than through breach of this Agreement; (c) is made known to the Party Recipient by a third party who had not obtained the information directly or indirectly from the Disclosing Party and does not obligate the Party Recipient to hold the same in confidence; or (d) is mutually agreed in writing by the parties to be non-confidential.
<br>
3.2	During the relevant Confidentiality Period, each Party Recipient shall use its best efforts to safeguard the Confidential Information of the Disclosing Party, using at least as great a degree of care as it uses to safeguard its own most Confidential Information.
<br>
3.3	Both parties acknowledge that the disclosure or use by a Party Recipient of any aspect of the Confidential Information of the Disclosing Party (other than as expressly permitted by this Agreement) shall immediately give rise to continuing irreparable injury to the Disclosing Party inadequately compensable in damages at law and without prejudice to any other remedy available to the Disclosing Party, and shall entitle the Disclosing Party to seek injunctive relief.
<br>
3.4	In the event that this Agreement is terminated, each Party Recipient shall cease to use the Confidential Information of the other party, and will return all originals and copies or extracts, summaries and the like made therefrom to the Disclosing Party.
 <br>

