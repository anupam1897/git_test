Article 4. Term and Termination <br></strong>
<br>
4.1	The term of this Agreement shall commence upon the Effective Date and shall continue for a period of five years, unless sooner terminated hereunder.
<br>
4.2	Either party may terminate this Agreement by prior written notice to the other party. In the event of such termination, neither party shall be liable to the other party for any failure to perform or for any additional payment hereunder, and each party shall bear its own costs and expenses incurred prior to such termination.
<br>
4.3	In the event of Termination, all obligations of either party to the other under this Agreement shall cease, other than (a) the obligation of Company to issue Cornell all Shares required to be issued through the effective date of termination under Section 2.2, (b) the obligations under Article 3, and (c) the indemnification obligations of the parties set forth in Article 7. In the event that Cornell terminates this  Agreement  prior  to  _ 	_ 	then Cornell shall be obligated to surrender to Company the number of Company Shares issued to Cornell pursuant to Sections 2.1 (i) and 2.1 (ii) in the same proportion that the number of days that the Agreement was terminated by Cornell prior  to __ 	bears to the number of days in the period from the date of this Agreement to _ 		_ _. This obligation to surrender Shares to Company shall not be effective in the event Cornell terminates this Agreement as a result of Company’s breach of any terms of this Agreement.
